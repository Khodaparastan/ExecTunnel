#!/usr/bin/env python3
from __future__ import annotations

import os
import subprocess
import sys
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[1]
DOCS_DIR = REPO_ROOT / "docs"
API_DIR = DOCS_DIR / "api"
CLI_DIR = DOCS_DIR / "cli"

API_MODULES = [
    "exectunnel",
    "exectunnel.defaults",
    "exectunnel.exceptions",
    "exectunnel.protocol",
    "exectunnel.protocol.enums",
    "exectunnel.protocol.frames",
    "exectunnel.protocol.ids",
    "exectunnel.transport",
    "exectunnel.transport.tcp",
    "exectunnel.transport.udp",
    "exectunnel.proxy",
    "exectunnel.proxy.config",
    "exectunnel.proxy.request",
    "exectunnel.proxy.server",
    "exectunnel.proxy.udp_relay",
    "exectunnel.session",
    "exectunnel.session.session",
    "exectunnel.observability",
    "exectunnel.observability.logging",
    "exectunnel.observability.metrics",
    "exectunnel.observability.tracing",
]

CLI_COMMANDS = [
    [],
    ["tunnel"],
    ["ws"],
    ["manager"],
    ["config"],
    ["config", "show"],
    ["config", "validate"],
    ["status"],
]

def ensure_dir(path: Path) -> None:
    path.mkdir(parents=True, exist_ok=True)

def write_text(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")

def module_to_doc_path(module: str) -> Path:
    rel = module.replace(".", "/") + ".md"
    return API_DIR / rel

def generate_api_pages() -> list[tuple[str, str]]:
    nav_entries: list[tuple[str, str]] = []
    for module in API_MODULES:
        out_path = module_to_doc_path(module)
        content = f"# `{module}`\n\n::: {module}\n"
        write_text(out_path, content)
        nav_entries.append((module, out_path.relative_to(DOCS_DIR).as_posix()))
    return nav_entries

def command_slug(parts: list[str]) -> str:
    if not parts:
        return "root"
    return "-".join(parts)

def command_title(parts: list[str]) -> str:
    if not parts:
        return "exectunnel"
    return "exectunnel " + " ".join(parts)

def run_help(parts: list[str]) -> str:
    cmd = [sys.executable, "-m", "exectunnel.cli", *parts, "--help"]
    env = os.environ.copy()
    env["TERM"] = "dumb"
    env["NO_COLOR"] = "1"
    proc = subprocess.run(
        cmd,
        cwd=REPO_ROOT,
        env=env,
        stdout=subprocess.PIPE,
        stderr=subprocess.STDOUT,
        text=True,
        encoding="utf-8",
        errors="replace",
        check=False,
    )
    return proc.stdout

def generate_cli_pages() -> list[tuple[str, str]]:
    nav_entries: list[tuple[str, str]] = []
    for parts in CLI_COMMANDS:
        slug = command_slug(parts)
        title = command_title(parts)
        out_path = CLI_DIR / f"{slug}.md"
        help_text = run_help(parts)
        content = f"# `{title}`\n\n```text\n{help_text}\n```\n"
        write_text(out_path, content)
        nav_entries.append((title, out_path.relative_to(DOCS_DIR).as_posix()))
    return nav_entries

def generate_api_index() -> None:
    lines = [
        "# API Reference",
        "",
        "## Main modules",
        "",
    ]
    for module in API_MODULES:
        rel = module.replace(".", "/") + ".md"
        lines.append(f"* [`{module}`]({rel})")
    lines.append("")
    write_text(API_DIR / "index.md", "\n".join(lines))

def generate_cli_index() -> None:
    lines = [
        "# CLI Reference",
        "",
        "## Commands",
        "",
    ]
    for parts in CLI_COMMANDS:
        slug = command_slug(parts)
        title = command_title(parts)
        lines.append(f"* [`{title}`]({slug}.md)")
    lines.append("")
    write_text(CLI_DIR / "index.md", "\n".join(lines))

def main() -> int:
    ensure_dir(API_DIR)
    ensure_dir(CLI_DIR)

    api_entries = generate_api_pages()
    cli_entries = generate_cli_pages()
    generate_api_index()
    generate_cli_index()

    print(f"Generated {len(api_entries)} API pages")
    print(f"Generated {len(cli_entries)} CLI pages")
    return 0

if __name__ == "__main__":
    raise SystemExit(main())
EOF

