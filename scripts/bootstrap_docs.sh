# Getting Started

## Requirements

* Python 3.12+
* Access to the Kubernetes exec/WebSocket endpoint or raw WebSocket URL
* A target environment where the remote agent can be started

## Basic tunnel

```bash
exectunnel tunnel
```

## Raw WebSocket mode

```bash
exectunnel ws wss://example.invalid/path
```

## Use the local SOCKS5 proxy

```bash
export https_proxy=socks5://127.0.0.1:1080
export http_proxy=socks5://127.0.0.1:1080
```
