# Architecture

## Layers

* `protocol` — frame codec, IDs, enums
* `transport` — TCP and UDP bridging
* `proxy` — SOCKS5 server and UDP associate handling
* `session` — bootstrap, reconnect, orchestration
* `observability` — logging, metrics, tracing
* `cli` — commands and dashboards

## Runtime flow

1. Start local SOCKS5 server
2. Establish WebSocket exec session
3. Bootstrap the remote agent
4. Wait for `AGENT_READY`
5. Relay TCP and UDP traffic through framed messages
6. Reconnect on transport failure
EOF

cat >docs/protocol.md <<'EOF'
# Protocol

ExecTunnel uses newline-delimited framed messages over the Kubernetes exec/WebSocket channel.

## High-level frame types

* `CONN_OPEN`
* `CONN_ACK`
* `DATA`
* `CONN_CLOSE`
* `UDP_OPEN`
* `UDP_DATA`
* `UDP_CLOSE`
* `ERROR`
* `KEEPALIVE`
* `AGENT_READY`
EOF

cat >docs/production.md <<'EOF'
# Production

## Recommendations

* Bind SOCKS5 to loopback unless exposure is intentional
* Use manager mode for supervised multi-tunnel deployments
* Tune reconnect behavior for your environment
* Prefer the Go agent where Python 3.12 is unavailable on target pods
* Monitor logs and metrics for reconnect churn and ACK timeout patterns
EOF

