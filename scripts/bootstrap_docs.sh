# Troubleshooting

## Common issues

### Agent never becomes ready

Check:

* remote Python availability
* bootstrap delivery mode
* pod shell behavior
* connectivity to fetch URL if using fetch mode

### SOCKS5 client fails to connect

Check:

* local bind host and port
* whether the tunnel session reached healthy state
* reconnect / ACK timeout behavior
EOF

